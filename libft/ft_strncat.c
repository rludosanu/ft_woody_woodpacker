#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;

	j = ft_strlen(s1);
	i = -1;
	while (s2[++i] != '\0' && i < n)
	{
		s1[j] = s2[i];
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
